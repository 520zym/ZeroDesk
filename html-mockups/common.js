const AVATAR_COLORS = [
  '#7B9EAA', '#8FA89E', '#C49A84', '#9B8EA8', '#C4B89A',
  '#7BA888', '#D4A76A', '#C47B7B', '#6A8D99', '#5A7D89',
];

function getAvatarColor(name) {
  let h = 0;
  for (let i = 0; i < name.length; i++) h = name.charCodeAt(i) + ((h << 5) - h);
  return AVATAR_COLORS[Math.abs(h) % AVATAR_COLORS.length];
}

function icon(name, size) {
  size = size || 20;
  return `<span class="nav-icon" style="width:${size}px;height:${size}px;display:inline-flex">${ICONS[name] || ''}</span>`;
}

function renderSidebar(activePage) {
  let html = `
    <div class="sidebar-logo">
      <div class="logo-dot">
        <svg width="16" height="16" viewBox="0 0 24 24" fill="none" stroke="white" stroke-width="2.5" stroke-linecap="round"><path d="M12 2L2 7l10 5 10-5-10-5z"/><path d="M2 17l10 5 10-5"/><path d="M2 12l10 5 10-5"/></svg>
      </div>
      <span>ZeroDesk</span>
    </div>
    <nav class="sidebar-nav">`;

  NAV_GROUPS.forEach(g => {
    html += `<div class="nav-group"><div class="nav-group-label">${g.label}</div>`;
    g.items.forEach(item => {
      const cls = item.id === activePage ? 'nav-item active' : 'nav-item';
      html += `<a class="${cls}" href="${item.href}">${icon(item.icon)}${item.label}</a>`;
    });
    html += `</div>`;
  });

  html += `</nav>
    <div class="sidebar-footer">
      <div class="avatar avatar-sm" style="background:var(--coral)">U</div>
      <div>
        <div style="font-weight:600;font-size:0.82rem;color:var(--text)">我的工作区</div>
        <div style="font-size:0.7rem;color:var(--text-muted)">本地运行中</div>
      </div>
    </div>`;

  return html;
}

